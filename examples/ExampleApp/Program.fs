/// Map each DU case to its ConfigVarDef.
let configDef =
    function
    | DatabaseUrl ->
        { Name = "DATABASE_URL"
          Kind = Manual
          ValueType = StringType
          Requirement = Required
          IsSecret = false

          Doc =
            { Description = "PostgreSQL connection string"
              HowToFind = "Check your database provider dashboard"
              ManagementUrl = None } }
    | DatabasePort ->
        { Name = "DATABASE_PORT"
          Kind = AutoGenerated(Some "5432")
          ValueType = IntType
          Requirement = Optional
          IsSecret = false

          Doc =
            { Description = "Database port number"
              HowToFind = "Usually 5432 for PostgreSQL"
              ManagementUrl = None } }
    | ApiKey ->
        { Name = "API_KEY"
          Kind = Manual
          ValueType = StringType
          Requirement = Required
          IsSecret = true

          Doc =
            { Description = "External API key for third-party service"
              HowToFind = "Generate at https://dashboard.example.com/keys"
