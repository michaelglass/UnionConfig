let demoVerification () =
    printfn "== Verification Results =="
    printfn ""

    // Build VerificationResult values for each config var
    let results =
        allConfigs
        |> List.map (fun config ->
            let def = configDef config

            let result =
                match def.Kind with
                | Infrastructure -> VerifySkipped "managed by infrastructure"
                | AutoProvisioned -> VerifySkipped "auto-provisioned by setup script"
                | Manual
                | AutoGenerated _ ->
                    match read def with
                    | Ok(Some _) -> VerifySuccess $"set (%A{def.ValueType})"
                    | Ok None -> VerifyFailed "not set"
                    | Error msg -> VerifyFailed msg

            (def.Name, result))
        |> Array.ofList

    // displayVerificationResults: colored output of verification state
    displayVerificationResults results

    printfn ""

